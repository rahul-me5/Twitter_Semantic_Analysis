            df = pd.read_excel(uploaded_file)
            
            # Predict sentiment and add a new column
            df['Predicted Sentiment'] = df['tweets'].apply(predict_sentiment)
            
            # Display the DataFrame with predictions
            st.write("Predicted Sentiments:")
