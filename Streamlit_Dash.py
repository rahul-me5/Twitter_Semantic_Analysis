# Function to predict sentiment
def predict_sentiment(text):
    # Preprocess the text
    cleaned_text = clean(text)
    
    # Transform the text using the TF-IDF vectorizer
    tfidf_vector = vectorizer.transform([cleaned_text])
    
    # Predict the sentiment class
    predicted_class = lg_model.predict(tfidf_vector)[0]
    
    # Map class labels to sentiment labels
    sentiment_labels = {0: "irony", 1: "regular", 2: "sarcasm"}
    predicted_sentiment = sentiment_labels.get(predicted_class, "Unknown")
    
    return predicted_sentiment

